st.subheader("üîé –ó–∞–ø—Ä–æ—Å—ã –ø–æ –ø—Ä–µ–¥–º–µ—Ç—É")
subjects = sorted(df_sorted['–ü—Ä–µ–¥–º–µ—Ç'].unique())
